package practicaltestOOPS;

public class Printer {
	
	public void printValue(int num) {
		System.out.println("Prints an integer number: "+num);
	}
	public void printValue(double num) {
		System.out.println("Prints a double number: "+num);
	}
	public void printValue(String text) {
		System.out.println("Prints a String :"+text);
	}
	public void printValue(boolean flag) {
		System.out.println("Prints a boolean value :"+flag);
	}
	

}
